import os
import uuid
from datetime import datetime, timezone
from pathlib import Path

import httpx

DRAFTS_DIR = os.getenv("DONNA_DRAFTS_DIR", "/gbio/donna/drafts")
DASHBOARD_IPC_URL = os.getenv("DONNA_SESSION_ROUTER_URL", "http://localhost:8000/ipc")
ATTORNEY_EMAIL = os.getenv("DONNA_ATTORNEY_EMAIL", "anish.gup05@gmail.com")

